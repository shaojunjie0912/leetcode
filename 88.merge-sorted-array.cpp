// NOTE:
// 非递减!!
// 从右往左倒序!  (nums1 最右边 m-1, nums2 最右边 n-1)
// NOTE: 思考, 从右往左确实能避免意外覆盖(memcpy)
// 不断比较 nums1[i] 和 nums2[j] 把较大的放入末尾

// @leet start
class Solution {
public:
    void merge(vector<int>& nums1, int m, vector<int>& nums2, int n) {
        int i = m - 1, j = n - 1, k = m + n - 1;
        while (i >= 0 && j >= 0) {
            if (nums1[i] > nums2[j]) {
                nums1[k--] = nums1[i--];
            } else {
                nums1[k--] = nums2[j--];
            }
        }
        // 把剩余的拷贝一下 NOTE: 用的是 while 不是 if
        while (i >= 0) {
            nums1[k--] = nums1[i--];
        }
        while (j >= 0) {
            nums1[k--] = nums2[j--];
        }
    }
};
